void Map() {

	SetDrawBlendMode(DX_BLENDMODE_ALPHA, 100);
	//RとBが当たったらマゼンタを描画
	if (map.isDraw[RED] == true && map.isDraw[BLUE] == true) {
		map.isDraw[MAGENTA] = true;
		}
	else {
		map.isDraw[MAGENTA] = false;
		}
	//RとGが当たったらイエローを描画
	if (map.isDraw[RED] == true && map.isDraw[GREEN] == true) {
		map.isDraw[YELLOW] = true;
		}
	else {
		map.isDraw[YELLOW] = false;
		}

	//BとGが当たったらシアンを描画
	if (map.isDraw[BLUE] == true && map.isDraw[GREEN] == true) {
		map.isDraw[CYAN] = true;
		}
	else {
		map.isDraw[CYAN] = false;
		}
	SetDrawBlendMode(DX_BLENDMODE_NOBLEND, 0);

	//マップ描画
	for (int y = 0; y < 30; y++) {

		for (int x = 0; x < 44; x++) {

			//ノーマルブロック
			if (map.CHIP[map.stage][y][x] == NORMAL) {
				DrawGraph(x * 32, y * 32, map.chipBlock[NORMAL], true);
				}

			if (map.chipBlock[R_LASER]) {
				//Rレーザー
				if (map.CHIP[map.stage][y][x] == R_LASER) {
					DrawGraph(x * 32, y * 32, map.chipBlock[R_LASER], true);
					}
				}

			if (map.chipBlock[G_LASER]) {
				//Gレーザー
				if (map.CHIP[map.stage][y][x] == G_LASER) {
					DrawGraph(x * 32, y * 32, map.chipBlock[G_LASER], true);
					}
				}

			if (map.chipBlock[B_LASER]) {
				//Bレーザー
				if (map.CHIP[map.stage][y][x] == B_LASER) {
					DrawGraph(x * 32, y * 32, map.chipBlock[B_LASER], true);
					}
				}
			if (map.CHIP[map.stage][y][x] == 11) {
				DrawGraph(x * 32, y * 32, map.chipBlock[11], true);
				}
			if (map.CHIP[map.stage][y][x] == 12) {
				DrawGraph(x * 32, y * 32, map.chipBlock[12], true);
				}
			if (map.CHIP[map.stage][y][x] == 13) {
				DrawGraph(x * 32, y * 32, map.chipBlock[13], true);
				}
			if (map.CHIP[map.stage][y][x] == 14) {
				DrawGraph(x * 32, y * 32, map.chipBlock[14], true);
				}
			if (map.CHIP[map.stage][y][x] == 15) {
				DrawGraph(x * 32, y * 32, map.chipBlock[15], true);
				}


			//SetDrawBlendMode(DX_BLENDMODE_ALPHA, 100);
			//赤ブロック
			//フラグが立ったら
			if (map.isDraw[RED] == true) {

				//赤を描画
				if (map.CHIP[map.stage][y][x] == RED) {
					DrawGraph(x * 32, y * 32, map.chipBlock[RED], true);
					}
				}

			//青ブロック
			//フラグが立ったら
			if (map.isDraw[GREEN] == true) {

				//青を描画
				if (map.CHIP[map.stage][y][x] == GREEN) {
					DrawGraph(x * 32, y * 32, map.chipBlock[GREEN], true);
					}
				}

			//緑ブロック
			//フラグが立ったら
			if (map.isDraw[BLUE] == true) {

				//緑を描画
				if (map.CHIP[map.stage][y][x] == BLUE) {
					DrawGraph(x * 32, y * 32, map.chipBlock[BLUE], true);
					}
				}

			//マゼンタブロック
			//フラグが立ったら
			if (map.isDraw[MAGENTA] == true) {

				//赤を描画
				if (map.CHIP[map.stage][y][x] == MAGENTA) {
					DrawGraph(x * 32, y * 32, map.chipBlock[MAGENTA], true);
					}
				}

			//イエローブロック
			//フラグが立ったら
			if (map.isDraw[YELLOW] == true) {

				//赤を描画
				if (map.CHIP[map.stage][y][x] == YELLOW) {
					DrawGraph(x * 32, y * 32, map.chipBlock[YELLOW], true);
					}
				}

			//シアンブロック
			//フラグが立ったら
			if (map.isDraw[CYAN] == true) {

				//赤を描画
				if (map.CHIP[map.stage][y][x] == CYAN) {
					DrawGraph(x * 32, y * 32, map.chipBlock[CYAN], true);
					}
				}
			//SetDrawBlendMode(DX_BLENDMODE_NOBLEND, 0);
			}
		}

	//クリスタル
	DrawGraph(map.crystalPos[map.stage][0] - 69, map.crystalPos[map.stage][1] - 96, map.crystalPic, true);
	}

